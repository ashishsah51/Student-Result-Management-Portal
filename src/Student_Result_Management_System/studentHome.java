    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try {
            String rollno = jTextField3.getText();
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "Ashish@1234");

            Statement st = con.createStatement();
            ResultSet rs=st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' and result.rollno='"+rollno+"'");
            //jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            if(rs.next()) {
                jTextField18.setText(rs.getString(2));
                jTextField1.setText(rs.getString(4));
                jTextField11.setText(rs.getString(3));
                jTextField12.setText(rs.getString(5));
                jTextField2.setText(rs.getString(6));
                
                jTextField6.setText(rs.getString(8));
                jTextField9.setText(rs.getString(9));
                jTextField13.setText(rs.getString(10));
                jTextField15.setText(rs.getString(11));
                jTextField16.setText(rs.getString(12));
                
                int s1 = Integer.parseInt(jTextField6.getText());
                int s2 = Integer.parseInt(jTextField9.getText());
                int s3 = Integer.parseInt(jTextField13.getText());
                int s4 = Integer.parseInt(jTextField15.getText());
                int s5 = Integer.parseInt(jTextField16.getText());
                
                String result = s1+s2+s3+s4+s5+"";
                String status = "PASS";
                if(s1<=30 || s2<=30 || s3<=30 || s4<=30 || s5<=30) status = "FAIL";
                jTextField14.setText(result);
                jTextField17.setText(status);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

